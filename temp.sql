DROP TABLE IF EXISTS City;
CREATE TABLE City (
  [Name] VARCHAR(255) NOT NULL,
  [Id] INT NOT NULL IDENTITY(1, 1),
  PRIMARY KEY (Id)
);
CREATE TABLE Drug (
  [DrugId] INT NOT NULL IDENTITY(1, 1),
  [Name] VARCHAR (255) NULL,
  PRIMARY KEY (DrugId)
);
DROP TABLE IF EXISTS Person;
CREATE TABLE Person (
  [PersonId] int not null identity(1, 1),
  [FirstName] VARCHAR(255) NOT NULL,
  [LastName] VARCHAR(255) NOT NULL,
  [NationalCode] INT NOT NULL UNIQUE,
  [YearOfBirth] int NOT NULL,
  [BirthCity] INT NOT NULL FOREIGN KEY REFERENCES City(Id),
  [City] INT NOT NULL FOREIGN KEY REFERENCES City(Id),
  PRIMARY KEY (PersonId)
);
CREATE TABLE Sign (
  [SignId] INT NOT NULL IDENTITY(1, 1),
  [Title] VARCHAR (255) NULL,
  PRIMARY KEY (SignId)
);
DROP TABLE IF EXISTS Patient;
CREATE TABLE Patient (
  [FirstDate] DATE NOT NULL,
  [Id] INT NOT NULL IDENTITY(1, 1),
  [CityId] INT NOT NULL FOREIGN KEY REFERENCES City(Id),
  [MutualPersonsCount] INT NOT NULL,
  [PersonId] INT NOT NULL FOREIGN KEY REFERENCES Person(PersonId),
  PRIMARY KEY (Id)
);
create table Confine (
  ConfineId int not null identity(1, 1),
  HospitalName varchar(255),
  ConfineDate date,
  ReleaseDate date,
  primary key (ConfineId)
);
CREATE TABLE Patient_Sign (
  [Id] INT NOT NULL IDENTITY(1, 1),
  PatientId int NOT NULL FOREIGN KEY REFERENCES Patient(Id),
  SignId int NOT NULL FOREIGN KEY REFERENCES Sign(SignId),
  primary key (Id)
);
CREATE TABLE Patient_Drug (
  Id INT NOT NULL IDENTITY(1, 1),
  PatientId int FOREIGN KEY REFERENCES Patient(Id),
  DrugId int Foreign Key References Drug(DrugId),
  ConfineId int Foreign key references Confine(ConfineId),
  primary key (Id)
);
create table PostPatience (
  Id int not null identity(1, 1),
  PatientId int not null Foreign key references Patient(Id),
  IsAlive bit,
  TheDate date,
  QuarantineStartDate date,
  QuarantineEndDate date,
  primary key (Id)
);
create table During_Patience (
  Id int not null identity(1, 1),
  PatientId int not null foreign key references Patient(Id),
  Confined int foreign key references Confine(ConfineId),
  primary key (Id)
);
DROP TABLE IF EXISTS Place;
CREATE TABLE Place (
  [Id] INT NOT NULL IDENTITY(1, 1),
  [CityId] INT NOT NULL FOREIGN KEY REFERENCES City(Id),
  [Name] VARCHAR(255) NOT NULL,
  PRIMARY KEY (Id)
);
DROP TABLE IF EXISTS Patient_Place;
CREATE TABLE Patient_Place (
  [Id] INT NOT NULL IDENTITY(1, 1),
  [PatinetId] INT NOT NULL FOREIGN KEY REFERENCES Patient(Id),
  [PlaceId] INT NOT NULL FOREIGN KEY REFERENCES Place(ID),
  PRIMARY KEY (Id)
);
DROP TABLE IF EXISTS Patient_Person;
CREATE TABLE Patient_Person (
  [PersonId] INT NOT NULL FOREIGN KEY REFERENCES Person(PersonId),
  [PatientId] INT NOT NULL FOREIGN KEY REFERENCES Patient(Id),
  [Id] INT NOT NULL IDENTITY(1, 1),
  PRIMARY KEY (Id)
);
DROP TABLE IF EXISTS Vaccination;
CREATE TABLE Vaccination (
  [Id] INT NOT NULL IDENTITY(1, 1),
  [PersonId] INT NOT NULL FOREIGN KEY REFERENCES Person(PersonId),
  [Date] DATE NOT NULL,
  [VaccanName] VARCHAR(255) NOT NULL,
  [Dose] INT NOT NULL,
  PRIMARY KEY (Id)
);
DROP TABLE IF EXISTS Patient_History;
CREATE TABLE Patient_History (
  [Id] INT NOT NULL IDENTITY(1, 1),
  [Alergy] bit NOT NULL,
  [Underlying_Disease] bit NOT NULL,
  [Special_Disease] bit NOT NULL,
  [Surgery] bit NOT NULL,
  [PatientId] INT NOT NULL FOREIGN KEY REFERENCES Patient(Id),
  PRIMARY KEY (Id)
);
